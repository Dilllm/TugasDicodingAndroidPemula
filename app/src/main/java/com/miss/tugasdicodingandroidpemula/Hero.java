package com.miss.tugasdicodingandroidpemula;

public class Hero {

    private String name;
    private String detail;
    private int photo;
    private String idTokoh;

    public String getIdTokoh() {
        return idTokoh;
    }

    public void setIdTokoh(String idTokoh) {
        this.idTokoh = idTokoh;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }
}
